defmodule Onicn.Elements.Isoresin do
  use Onicn.Categories.Solid

  section "简介" do
    "异构树脂是一种稀有资源，只能自太空任务中获得。"
  end

  section "用途" do
    "分子熔炉能够用异构树脂、芦苇纤维和深渊晶石合成隔热质。15 千克 异构树脂 + 5 单位 芦苇纤维 + 80 千克 深渊晶石 = 100 千克 隔热质。"
    "分子熔炉能够用异构树脂和石油合成粘性凝胶。35 千克 异构树脂 + 65 千克 石油 = 100 千克 粘性凝胶。"
  end

  section "小技巧" do
    "应该保存在100 °C以下的环境，否则会变为石脑油并且不可逆。"
  end
end
