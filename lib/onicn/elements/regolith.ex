defmodule Onicn.Elements.Regolith do
  use Onicn.Categories.Solid,
    cn_name: "浮土"

  section "简介" do
    "浮土是一种过滤介质，只存在于太空区。"
  end

  section "用途" do
    "浮土可以用于净水器来将污染水过滤成清水。5 千克/秒 污染水 + 1 千克/秒 沙子 或 浮土 = 5 千克/秒 水 + 200 克/秒 污染土。"
    "浮土可以用于空气净化器来将污染氧过滤成氧气。100 克/秒 污染氧 + 133.33 克/秒 沙子 或 浮土 = 90 克/秒 氧气 + 143.33 克/秒 粘土。"
    "浮土可以作为饲料喂食给锹环田鼠，锹环田鼠会排泄出质量减半的整格浮土。"
  end

  section "小技巧" do
    "陨石雨会带来大量高热量的浮土。这些浮土能够导致所接触的建筑过热。玩家需要找到某种方法保护建筑，并处理掉多余的浮土和热量。"
  end
end
