defmodule Onicn.Elements.SandStone do
  use Onicn.Categories.Solid,
    cn_name: "砂岩"

  section "简介" do
    "砂石是一种矿物原料，大量存在于常温区。"
  end

  section "用途" do
    "在一些小行星类型中，砂石是玩家遇到的第一种矿物原料，可以用来建造各种建筑。砂石有着装饰+10%的奖励，可用于建造和装饰相关的建筑。"
    "注意砂石硬度较低，不适合承受太大压力，在修建液体池塘时需避免用砂石作为底部砖块材料。"
    "碎石机能够将砂石粉碎成沙子。100 千克 砂石 = 100 千克 沙子。"
    "砂石可以作为饲料喂食给好吃哈奇，好吃哈奇会排泄出煤炭。"
  end
end
