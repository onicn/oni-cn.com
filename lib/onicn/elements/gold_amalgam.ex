defmodule Onicn.Elements.GoldAmalgam do
  use Onicn.Categories.Solid,
    cn_name: "金汞齐"

  section "简介" do
    "金汞齐是一种金属矿石，常见于沼泽区。"
  end

  section "用途" do
    "作为一种建筑材料，金汞齐使建筑的过热温度提高50摄氏度，适用于环境温度在125摄氏度以下的场合。"
    "金属精炼器能用金汞齐精炼成金。100 千克 金汞齐 = 100 千克 金。"
    "碎石机能够将金汞齐粉碎成金和沙子。100 千克 金汞齐 = 50 千克 金 + 50 千克 沙子。"
    "金汞齐可以作为饲料喂食给石壳哈奇，石壳哈奇会排泄出煤炭。当进食金属矿石时，石壳哈奇会提高产下光滑哈奇蛋的概率。"
    "金汞齐可以作为饲料喂食给光滑哈奇，光滑哈奇会排泄出金。"
  end

  section "生产" do
    "太空区的陨石雨会带来金汞齐。"
  end
end
