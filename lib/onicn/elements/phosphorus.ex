defmodule Onicn.Elements.Phosphorus do
  use Onicn.Categories.Solid,
    cn_name: "固态磷"

  section "简介" do
    "固态磷是一种消耗性矿石。它不是游戏世界中的天然矿物，需要人工制备才能获得。"
  end

  section "用途" do
    "固态磷可以作为饲料喂食给天蓝虫和深渊虫。使用固态磷作为诱饵建造飞行动物诱饵可以吸引天蓝虫和深渊虫。"
  end

  section "生产" do
    "先把磷矿熔化得到液态磷，再将液态磷冷却凝固就得到固态磷。"
  end
end
