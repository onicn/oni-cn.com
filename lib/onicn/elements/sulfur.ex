defmodule Onicn.Elements.Sulfur do
  use Onicn.Categories.Solid,
    cn_name: "硫"

  section "简介" do
    "硫是由酸性天然气制备液态甲烷的副产品。"
  end

  section "用途" do
    "人工种植虫果需要施肥硫。"
    "硫可以作为饲料喂给甜素甲虫和虫果果虫。"
  end

  section "生产" do
    "硫喷泉会定期喷发液态硫。"
    "将酸性天然气冷却至 -163.5 摄氏度（凝点 -2 摄氏度）时会得到液态甲烷（67% 质量）和硫（33% 质量）。"
  end
end
