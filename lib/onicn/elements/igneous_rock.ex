defmodule Onicn.Elements.IgneousRock do
  use Onicn.Categories.Solid,
    cn_name: "火成岩"

  section "简介" do
    "火成岩是一种矿物原料，大量存在于腐蚀区和原油区。"
  end

  section "用途" do
    "作为一种建筑材料，火成岩有着较高的比热容和较低的导热系数，适合在游戏早期减缓热传递，尤其适合隔热砖和隔热管道。"
    "在各种矿物原料中，火成岩的储量是最高的。"
    "碎石机能够将火成岩粉碎成沙子。100 千克 火成岩 = 100 千克 沙子。"
    "火成岩可以作为饲料喂食给石壳哈奇，石壳哈奇会排泄出煤炭。"
  end

  section "生产" do
    "火山喷发的岩浆冷却后会形成火成岩。也可以融化浮土生成岩浆再冷却形成火成岩。"
  end
end
