defmodule Onicn.Elements.Granite do
  use Onicn.Categories.Solid,
    cn_name: "花岗岩"

  section "简介" do
    "花岗岩是一种矿物原料。在起始区域周围有一圈“花岗岩圈”。花岗岩也常见于冰冻区、原油区和贫瘠区。"
    "复制人需要学习坚硬材料挖掘技能才能挖掘花岗岩。"
  end

  section "用途" do
    "作为一种建筑材料，花岗岩拥有较低的比热容和较高的导热率，适合热传导。"
    "花岗岩有+20%的装饰奖励，适合于建造和装饰相关的建筑。"
    "大理石雕块需要400千克花岗岩或黑曜石作为建筑材料。"
    "碎石机能够将花岗岩粉碎成沙子。100 千克 花岗岩 = 100 千克 沙子。"
    "花岗岩可以作为饲料喂食给石壳哈奇，石壳哈奇会排泄出煤炭。"
  end
end
