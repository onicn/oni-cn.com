defmodule Onicn.Elements.ViscoGel do
  use Onicn.Categories.Liquid,
    cn_name: "粘性凝胶"

  section "简介" do
    "粘性凝胶是一种特殊的液体。和其他液体不同，粘性凝胶不会水平流动，而是会形成近似每格100千克的竖直排列。"
  end

  section "用途" do
    "利用粘性凝胶的上述特性，玩家可以实现许多特殊设计。经常用来制作水门隔绝液体两边的气体交换。"
  end

  section "生产" do
    "分子熔炉能够用异构树脂和石油合成粘性凝胶。35 千克 异构树脂 + 65 千克 石油 = 100 千克 粘性凝胶。"
  end
end
