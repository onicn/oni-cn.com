defmodule Onicn.Elements.Clay do
  use Onicn.Categories.Solid,
    cn_name: "粘土"

  section "简介" do
    "粘土是一种可耕作的泥土。"
  end

  section "用途" do
    "粘土的主要用途是在窑炉中烧制陶瓷。100 千克 粘土 + 25 千克 煤炭 = 100 千克 陶瓷。"
    "粘土可以用来建造下列建筑：种植箱：100 千克。土培砖：100 千克。藻类箱：400 千克。"
    "粘土可以作为饲料喂食给好吃哈奇，好吃哈奇会排泄煤炭。"
  end

  section "生产" do
    "在沼泽区中有数量不等的粘土块可供挖掘。"
    "空气净化器会产出粘土作为过滤污染氧的副产品。100 克/秒 污染氧 + 133.33 克/秒 沙子 或 浮土 = 90 克/秒 氧气 + 143.33 克/秒 粘土."
  end
end
