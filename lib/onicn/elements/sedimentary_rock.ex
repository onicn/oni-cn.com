defmodule Onicn.Elements.SedimentaryRock do
  use Onicn.Categories.Solid

  section "简介" do
    "沉积岩是一种矿物原料，常见于沼泽区和盐池区。"
  end

  section "用途" do
    "沉积岩特性平平无奇，适合在沼泽区和盐池区就近建造各种建筑。注意它的硬度较低，不适合作为液体池塘的底部砖块材料。"
    "沉积岩可以作为饲料喂食给好吃哈奇，好吃哈奇会排泄出煤炭。喂食沉积岩会提高好吃哈奇生下石壳哈奇蛋的概率。"
    "碎石机能够将沉积岩粉碎成沙子。100 千克 沉积岩 = 100 千克 沙子。"
  end

  section "生产" do
    "碎石机能够将化石粉碎成石灰和沉积岩。100 千克 化石 = 5 千克 石灰 + 95 千克 沉积岩。"
  end
end
