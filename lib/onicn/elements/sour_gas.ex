defmodule Onicn.Elements.SourGas do
  use Onicn.Categories.Gas

  section "简介" do
    "高硫天然气是一种不可呼吸的气体，由石油加热而得。在原油区底部有天然高硫天然气存在。 "
  end

  section "用途" do
    "高硫天然气是从原油制取天然气的转化路径中的一环。将高硫天然气冷却至 -163.5 摄氏度（凝结点 -2 摄氏度）会得到液态甲烷（67%质量）和硫（33%质量）。将液态甲烷加热至 -159.5 摄氏度（沸点 +2 摄氏度）会得到天然气。"
  end

  section "生产" do
    "石油和石脑油加热至 540.85 摄氏度（沸点 +2 摄氏度）会生成高硫天然气。"
  end
end
