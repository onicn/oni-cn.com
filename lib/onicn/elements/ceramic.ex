defmodule Onicn.Elements.Ceramic do
  use Onicn.Categories.Solid

  section "简介" do
    "陶瓷是一种人造的隔热建筑材料，游戏中期以后可以制造。"
  end

  section "用途" do
    "作为一种建筑材料，陶瓷使建筑的过热温度增加200摄氏度，并使得装饰度增加20%。陶瓷的导热率较低，可以减缓热传递。"
    "两个对象之间的导热率由较低的一方决定，因此陶瓷可以有效减少热传递。用陶瓷，黑曜石或隔热质建造的砖可以安全地放到岩浆里。"
    "碎石机能够将陶瓷粉碎成沙子。100 千克 陶瓷 = 100 千克 沙子。由于陶瓷比沙子更难获得所以不建议这样做。"
    "建造丰碑中段需要 2500 千克 陶瓷和其它一些材料。"
  end

  section "生产" do
    "窑炉能够用粘土和煤炭烧制陶瓷。100 千克 粘土 + 25 千克 煤炭 = 100 千克 陶瓷。"
    "加热粘土到 929.85 °C 可以变成陶瓷。"
  end
end
