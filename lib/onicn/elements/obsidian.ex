defmodule Onicn.Elements.Obsidian do
  use Onicn.Categories.Solid,
    cn_name: "黑曜石"

  section "简介" do
    "黑曜石是一种矿物原料，常见于贫瘠区和火山区中。"
    "具有小型石块/中型石块/大型石块/混合型石块特质的小行星会有黑曜石团块散布在地图中。"
    "复制人需要学习极坚硬材料挖掘技能才能挖掘黑曜石。"
  end

  section "用途" do
    "黑曜石熔点极高，可用于建造火山和岩浆附近的建筑。"
    "大理石雕块需要400千克花岗岩或黑曜石作为建筑材料。"
    "碎石机能够将黑曜石粉碎成沙子。100 千克 黑曜石 = 100 千克 沙子。"
    "黑曜石可以作为饲料喂食给石壳哈奇，石壳哈奇会排泄出煤炭。"
  end
end
