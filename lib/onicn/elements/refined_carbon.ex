defmodule Onicn.Elements.RefinedCarbon do
  use Onicn.Categories.Solid

  section "简介" do
    "精炼碳是一种消耗性矿石。它是炼钢的原料之一。"
  end

  section "用途" do
    "金属精炼器能用铁、石灰和精炼碳来生产钢。70 千克 铁 + 20 千克 精炼碳 + 10 千克 石灰 = 100 千克 钢。"
  end

  section "生产" do
    "窑炉能将煤炭精炼成精炼碳。125 千克 煤 = 100 千克 精炼碳。"
    "另一种方法是将煤炭加热到278.85摄氏度（熔点+2摄氏度），煤炭会转化成精炼碳。"
  end
end
