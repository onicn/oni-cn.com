  end

  section "生产" do
    "分子熔炉能够用深渊晶石和异构树脂合成隔热质。15 千克 异构树脂 + 80 千克 深渊晶石 + 5 单位 芦苇纤维 = 100 千克 隔热质。"
    "异构树脂需要通过太空任务才能获得。"
  end
