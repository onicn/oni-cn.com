defmodule Onicn.Buildings.WaterPurifier do
  use Onicn.Categories.Building

  section "简介" do
    "净水器用过滤介质净化污染水，从输出管道输出水，产生的污染土会掉在地上。"
  end

  section "用途" do
  end

  section "小技巧" do
    "如果过滤介质用完了，净水器会停止接收输入管道的液体。"
    "输入盐水，浓盐水或水不会损坏净水器，会直接输出并保持温度不变。"
    "净水器不会移除病菌，最好将带菌的水和干净的水分开存放。"
    "1根水管可以供应两个净水器。"
    "1个净水器可以配合5个碳素脱离器。产生的污染土需要两个堆肥处理。"
    "1个净水器可以每周期可以净化3吨水。可以处理300次抽水马桶和洗手池组合，或者100个淋浴间的产生的污染水。"
  end
end
