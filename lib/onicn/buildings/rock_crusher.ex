defmodule Onicn.Buildings.RockCrusher do
  use Onicn.Categories.Building

  section "简介" do
    "碎石机可以用矿物原料和金属矿石生产沙子和精炼金属，也可以生产食盐和石灰。"
    "需要复制人操作。"
  end

  section "用途" do
    "生产食盐和石灰，在前期生产精炼金属。"
  end

  section "小技巧" do
    "碎石机金属矿石到精炼金属的转化率只有50%，用金属精炼器可以产生更多精炼金属。由于炼钢仍然需要碎石机生产石灰，因此金属精炼器不能完全替代碎石机。"
  end
end
