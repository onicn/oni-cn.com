defmodule Onicn.Buildings.InsulatedLiquidConduit do
  use Onicn.Categories.Building

  section "简介" do
    "隔热液体管道能够大幅减缓管道内液体和周围环境的热交换，保持管道内液体温度相对恒定。"
    "类似于隔热砖，隔热液体管道拥有大幅降低（3.125%）的导热系数，以及隔热属性。"
    "建造所需的材料为普通气体管道的4倍。"
  end

  section "用途" do
    "保持管道内液体温度，避免热交换。"
    "玻璃熔炉，金属精炼器和液温调节器附近的管道通常需要用隔热液体管道。"
  end
end
