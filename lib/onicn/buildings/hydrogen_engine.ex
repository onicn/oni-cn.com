defmodule Onicn.Buildings.HydrogenEngine do
  use Onicn.Categories.Building

  section "简介" do
    "液氢引擎是一个最强大的火箭引擎，建造在火箭最下方为火箭提供动力。"
    "它排出1726.9°C（2000 K）的过热蒸汽。 在整个起飞过程中，发动机下方的区域将被加热到2926.9°C（3200 K）的最高温度。"
    "与蒸汽引擎不同，液氢引擎本身不存储燃料。 需要额外的液缸燃料舱来存储液态氢。另外需提供氧石或液态氧作为氧化剂。 如果使用氧石，则将存储在固态氧化剂舱中；如果使用液态氧，则存储在液态氧化剂舱中。"
  end
end
