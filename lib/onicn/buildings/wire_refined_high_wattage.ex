defmodule Onicn.Buildings.WireRefinedHighWattage do
  use Onicn.Categories.Building

  section "简介" do
    "高负荷导线是高负荷电线的升级版，负载功率达到50千瓦，建造需要精炼金属，装饰度比高负荷电线稍微高一点（但是依然很低）。"
    "高负荷导线不能隐藏在砖块中，而需要使用高负荷导线接合板穿墙。"
  end

  section "用途" do
    "用于发电中心连接发电机、电池、功率转换器。"
  end

  section "小技巧" do
    "因为装饰度很低，不适合建造在复制人经常呆的地方，例如人力发电机附近。"
    "不能穿过墙和门，要提前规划好高负荷导线接合板的位置。"
    "用金建造可以减少50%的装饰度惩罚。"
  end
end
