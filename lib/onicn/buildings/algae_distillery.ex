defmodule Onicn.Buildings.AlgaeDistillery do
  use Onicn.Categories.Building

  section "简介" do
    "藻类蒸馏器能够将菌泥转化为藻类和污染水。污染水通过出水口输出，藻类会掉到地上，每次30kg。"
    "里面存储的菌泥不会挥发出污染氧。"
  end

  section "用途" do
    "藻类蒸馏器产出两种重要的资源，因此有建造的价值。效率不高，如果你有大量的菌泥，可能会需要建造多台藻类蒸馏器。"
  end

  section "小技巧" do
    "装饰度低，并且产热快，建议远离基地建造。"
    "两种产出的资源都可能带有粘液肺病菌。它们通常会逐渐死亡。如果为了保险起见，可以利用氯气环境快速杀菌。"
    "可以在藻类蒸馏器的两边种植同伴芽。因为同伴芽会释放花香病菌，每个格子只能有一种病菌，因此粘液肺病菌会被封闭在内部，防止其扩散。"
  end
end
