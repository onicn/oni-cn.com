defmodule Onicn.Buildings.BottleEmptierGas do
  use Onicn.Categories.Building

  section "简介" do
    "空罐器类似于空瓶器，可以作为复制人运送的罐装气体的目标地点，把罐装气体释放到世界中。"
    "可以设定空罐器接收的气体类型。"
    "气体罐装器可以将气体装罐以供搬运。"
  end
  section "用途" do
    "利用它复制人可以从某地点抽取少量气体并且搬运到较远的地方释放出来，避免长距离建造气体管道。"
    "可以在芳香百合种植区或毛鳞壁虎养殖区建造空罐器，从而将其它地方收集到的氯气或氢气等养殖需要的气体释放到空罐器所在房间。"
  end
  section "小技巧" do
    "释放气体没有最大压强限制，因此可以用于在有液封门（水门）的小房间内存储大量气体，但是这需要复制人操作，因此不太实用。"
  end
    
end
