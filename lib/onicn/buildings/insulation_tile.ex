defmodule Onicn.Buildings.InsulationTile do
  use Onicn.Categories.Building

  summary "隔热砖可以大幅度减缓热量传递。跟普通砖块相比需要更多材料并且装饰度较低。"
  summary "隔热砖拥有特殊的隔热属性，其导热系数降为材料导热系数的1%。例如，火成岩的导热系数是2，制成隔热砖后，隔热砖的导热系数是0.02。"

  usage "隔热砖常用于建造基地的外围砖、氢气制冷室、间歇泉开发等需要阻断热传递的场合。一般最常用的材料是火成岩，因其大量存在。陶瓷比火成岩性能更好，但需消耗煤和粘土。"

  tips "游戏后期可以用隔热质制造的普通砖块代替隔热砖。"
  tips "深渊晶石通常起到了在不同生态区隔绝温度交换的作用，在挖开之后温度扩散会比较快，尤其是岩浆区，挖开后最好用隔热砖围起来。"
end
