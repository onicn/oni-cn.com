defmodule Onicn.Buildings.MineralDeoxidizer do
  use Onicn.Categories.Building

  section "简介" do
    "氧气扩散器是最早的生产氧气的建筑。"
    "氧气扩散器消耗藻类生成氧气。"
    "当周围气压达到1800克时，氧气扩散器会因气压过高而暂停工作。"
    "藻类中含有的黏液肺病菌会一并转移到氧气中，因为病菌会在氧气中逐渐死亡。"
    "生成的氧气至少为30摄氏度。"
  end

  section "用途" do
    "在游戏早期，氧气扩散器是主要的供氧方式。"
  end

  section "小技巧" do
    "氧气扩散器对藻类需求很大，玩家需要适时切换到电解水供氧的方式。"
  end
end
