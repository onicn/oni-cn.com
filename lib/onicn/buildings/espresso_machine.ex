defmodule Onicn.Buildings.EspressoMachine do
  use Onicn.Categories.Building

  section "简介" do
    "浓缩咖啡机是娱乐建筑。复制人可以在休息时间使用浓缩咖啡机。每次使用消耗1千克火椒粒和1千克水。"
    "提供“喝了浓缩咖啡”的状态，提高4点士气和1点运动属性。"
  end

  section "小技巧" do
    "如果输入的水中含有食物中毒病菌，复制人使用后可能会食物中毒。"
    "用水量比汽水机要小。"
    "只有复制人使用的时候才会耗电。"
    "使用时间大约有一格，过程中其它复制人不能使用这个机器，并且可能会占用后面的睡觉或工作时间。"
  end
end
