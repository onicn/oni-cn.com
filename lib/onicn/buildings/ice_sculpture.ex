defmodule Onicn.Buildings.IceSculpture do
  use Onicn.Categories.Building

  section "简介" do
    "冰雕块是装饰性建筑，比雕塑块提供的装饰度更多，但是不在低温环境的话会融化，提高的装饰度取决于艺术家的技能等级。"
  end

  section "小技巧" do
    "在-0.6摄氏度以上会融化，因此只适合在长期保持0度以下的环境中建造。"
    "可以在殖民地的水源上方建造网格砖，在网格砖上面建造冰雕块，这样可以降低周围的温度，并且可以训练艺术家，把冰转化为水。"
    "沙盒模式会以20摄氏度来创建新建筑，因此摆放冰雕块会马上融化。"
  end
end
