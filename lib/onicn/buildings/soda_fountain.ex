defmodule Onicn.Buildings.SodaFountain do
  use Onicn.Categories.Building

  section "简介" do
    "汽水机是娱乐建筑。复制人可以在休息时间使用汽水机。每次使用消耗1千克二氧化碳和5千克水。"
    "提供“喝了浓缩咖啡”的状态，提高4点士气和1点科学属性。"
  end

  section "小技巧" do
    "如果输入的水中含有食物中毒病菌，复制人使用后可能会食物中毒。"
    "因为没有输入气体管道，因此需要气体罐装器和人力搬运。"
    "耗水量比榨汁机和浓缩咖啡机多很多。"
    "可以用于消耗二氧化碳，但是比碳素脱离器效率低。"
  end
end
