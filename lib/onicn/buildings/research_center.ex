defmodule Onicn.Buildings.ResearchCenter do
  use Onicn.Categories.Building

  section "简介" do
    "复制人在研究站消耗泥土产生基础研究点数，可以解锁新的科技，在游戏开始时就可以建造。"
  end

  section "小技巧" do
    "多个研究站可以同时研究提高研究速度。不过一般情况下一个研究站就够了。"
    "科学属性高的复制人可以提高研究速度。可以在游戏开始时就选择一个高科学属性的复制人。"
    "使用科学属性低的复制人进行研究也是一种策略，研究增加的属性可以帮助它们之后更快升级。"
    "更高科学属性的复制人会自动替换掉低科学属性的复制人进行研究工作。"
    "研究站建在打印舱旁边可以利用打印舱的光照增加研究速度。"
    "复制人的位置判定点在左下角。"
  end
end
