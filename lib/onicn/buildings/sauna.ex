defmodule Onicn.Buildings.Sauna do
  use Onicn.Categories.Building

  section "简介" do
    "桑拿浴室属于娱乐建筑，复制人在休息时间可进行使用。"
    "使用过程中压力 -10%/周期。"
    "使用后获得“蒸汽力量”状态，效果为增加2点士气，持续3.95周期。"
    "桑拿浴室最多可供 1 名复制人同时使用，每次使用消耗25千克蒸汽，产生80摄氏度的水。"
  end

  section "小技巧" do
    "桑拿浴室可以消除热量。"
    "作为娱乐建筑，比较适合放置在娱乐室里。但是娱乐室并不提供任何加成，只是提供一个社交场所让休息的复制人聚集在一起。"
    "士气加成状态期间复制人会优先使用其它娱乐建筑，没有其它娱乐建筑时也可能再次使用该建筑。"
  end
end
