defmodule Onicn.Buildings.Polymerizer do
  use Onicn.Categories.Building

  section "简介" do
    "聚合压机用石油生产塑料，向环境排放蒸汽，从管道排出二氧化碳。"
  end

  section "小技巧" do
    "产出物温度很高因此需要配备冷却设施，防止设备过热或产出的塑料融化。"
    "建议用钢来制造聚合压机，不仅过热温度高，而且导热率和比热容都更好。但是仍然要防止温度超过150摄氏度而导致塑料融化。"
    "也可以用养滑鳞壁虎剪毛的方式来生产塑料，缺点是速度比聚合压机慢。"
  end
end
