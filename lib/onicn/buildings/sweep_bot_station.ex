defmodule Onicn.Buildings.SweepBotStation do
  use Onicn.Categories.Building

  section "简介" do
    "扫扫基站可以给扫扫机器人充电和回收杂物。需要消耗 25 千克精炼金属来制造一个扫扫机器人。"
    "如果扫扫基站被拆除，扫扫机器人会爆炸并掉落部分建造材料。"
    "扫扫机器人可以清扫液体，最多可以清扫 10 千克物品。"
    "扫扫基站只有在扫扫机器人充电时消耗电力。"
    "在扫扫基站回收了扫扫机器人清扫的杂物后，复制人可以取走。"
  end

  section "小技巧" do
    "比较适合放在经常有东西或液体掉落的地方。但是清扫速度很慢。"
    "扫扫机器人不可以开门和爬梯子，也不可以跳跃。"
    "扫扫机器人头顶可以放一个物品用于展示，可以将太空任务带回来的工艺品放在上面。"
  end
end
