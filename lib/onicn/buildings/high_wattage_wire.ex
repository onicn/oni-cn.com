defmodule Onicn.Buildings.HighWattageWire do
  use Onicn.Categories.Building

  section "简介" do
    "高负荷电线的负载功率达到20千瓦，但建造成本高昂，而且装饰度处罚达到-25。"
    "高负荷电线不能隐藏在砖块中，而需要使用高负荷电线接合板穿墙。"
  end

  section "用途" do
    "用于发电中心连接发电机、电池、功率转换器。"
  end

  section "小技巧" do
    "因为装饰度很低，不适合建造在复制人经常呆的地方，例如人力发电机附近。"
    "不能穿过墙和门，要提前规划好高负荷电线接合板的位置。"
  end
end
