defmodule Onicn.Buildings.FlowerVaseHangingFancy do
  use Onicn.Categories.Building

  section "简介" do
    "透明花盆是装饰性建筑，需要放在天花板上，可以在里面种植装饰性植物提高周围的装饰度，降低复制人压力。"
    "建筑本身不提供装饰度，装饰度来源于种植的植物。"
    "如果种植的植物因生长需求达不到要求而枯萎，提供的装饰度会降低。"
    "可以种植的种子有：诱人荆棘种子，欢乐叶种子，雀跃掌种子，同伴芽种子，孢子兰种子。"
  end

  section "小技巧" do
    "雀跃掌对环境温度要求最宽松。"
    "是垂吊花盆的升级版，可以提供更大的装饰度范围。"
  end
end
