defmodule Onicn.Buildings.WireBridgeHighWattage do
  use Onicn.Categories.Building

  section "简介" do
    "高负荷电线接合板和高负荷电线配合使用，让后者可以穿过地板和墙壁。"
    "高负荷电线接合板左右各有一个连接点可以连接高负荷电线，也可旋转至竖直位置。"
    "高负荷电线接合板可以作为砖块支撑上方建筑。"
  end

  section "用途" do
    "让高负荷电线可以穿过地板和墙壁。"
  end

  section "小技巧" do
    "跟电线桥类似，中间的格子不算做有高负荷电线通过，因此可以垂直方向穿过其它电线。"
    "由于是由金属制造，导热率较高，如果和隔热砖一起放会导致热量漏出。在特别需要隔热的房间，可以让接合板的一端放在真空环境来防止热量传递。"
    "建造材料里导热率最低的是金汞齐。"
  end
end
